package br.com.javacompleto.introducao._006Operadores;

public class _006Operadores {
    public static void main (String [] args){
        int numero1 = 10;
        int numero2 = 20;
        System.out.println(numero1 + numero2 + " é a Soma de: " + numero1 + numero2 + " ...OPS! Concatenou!!!");
        System.out.println("A soma é: " + (numero1+numero2)); //Também poderia usar uma variável soma.
        System.out.println("A Subtração é: " + (numero1-numero2));
        System.out.println("A Multiplicação é: " + (numero1*numero2));
        System.out.println("A Divisão é: " + (numero1/numero2)); // Resultado é um número inteiro.
        System.out.println("O resto dessa Divisão é: " + (numero1%numero2));



    }
}
